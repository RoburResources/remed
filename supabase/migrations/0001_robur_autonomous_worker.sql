create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.goals (
  id bigint generated by default as identity primary key,
  goal_text text not null,
  status text not null default 'active' check (status in ('active', 'paused', 'completed', 'archived')),
  sub_goals jsonb not null default '[]'::jsonb,
  priority integer not null default 5 check (priority between 1 and 10),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.task_queue (
  id bigint generated by default as identity primary key,
  goal_id bigint references public.goals(id) on delete set null,
  source text not null default 'task_generator',
  description text not null,
  priority_score numeric(6, 2) not null default 50 check (priority_score between 0 and 100),
  status task_status not null default 'pending',
  assigned_agent text,
  action_type task_action_type not null default 'web_research',
  action_payload jsonb not null default '{}'::jsonb,
  result_summary text,
  metadata jsonb not null default '{}'::jsonb,
  estimated_value numeric(12, 2) not null default 0,
  external_contact boolean not null default false,
  approval_required boolean not null default false,
  approval_nonce_hash text,
  approval_expires_at timestamptz,
  approval_used_at timestamptz,
  approval_requested_at timestamptz,
  attempt_count integer not null default 0 check (attempt_count >= 0),
  max_attempts integer not null default 3 check (max_attempts between 1 and 10),
  idempotency_key text unique,
  claimed_at timestamptz,
  lease_expires_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  completed_at timestamptz
);

alter table public.task_queue
