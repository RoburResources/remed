create or replace function public.claim_next_task(worker_id text, lease_seconds integer default 900)
returns setof public.task_queue
language plpgsql
security definer
set search_path = public
as $$
begin
  if coalesce(
    (select (config_value #>> '{}')::boolean from public.system_config where config_key = 'kill_switch_active'),
    true
  ) then
    return;
  end if;

  return query
  with next_task as (
    select id
    from public.task_queue
    where status = 'pending'
      and (lease_expires_at is null or lease_expires_at < now())
    order by priority_score desc, created_at asc
    for update skip locked
    limit 1
  )
  update public.task_queue tq
  set
    status = 'in_progress',
    assigned_agent = worker_id,
    claimed_at = now(),
    lease_expires_at = now() + make_interval(secs => lease_seconds),
    updated_at = now()
  from next_task
  where tq.id = next_task.id
  returning tq.*;
end;
$$;

create or replace function public.increment_daily_metric(metric_name text, amount integer default 1)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if metric_name not in (
    'tasks_generated',
    'tasks_completed',
    'tasks_failed',
    'calls_made',
    'emails_sent',
    'sms_sent',
    'api_spend_cents'
  ) then
    raise exception 'Unsupported metric: %', metric_name;
  end if;

  execute format(
    'insert into public.daily_metrics(date, %I) values (current_date, $1)
     on conflict(date) do update set %I = public.daily_metrics.%I + $1, updated_at = now()',
    metric_name,
    metric_name,
    metric_name
  )
  using amount;
end;
$$;

create or replace function public.expire_old_approvals()
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare changed integer;
begin
  update public.task_queue
  set
    status = 'cancelled',
    result_summary = coalesce(result_summary, 'Approval expired'),
    updated_at = now()
  where status = 'awaiting_approval'
    and approval_expires_at is not null
    and approval_expires_at < now();

  get diagnostics changed = row_count;
  return changed;
end;
$$;

create or replace function public.recover_stale_in_progress_tasks(
  stale_after_seconds integer default 1800,
  default_max_attempts integer default 3
)
returns table(task_id bigint, action text, attempt_count integer, max_attempts integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  rec record;
  next_attempt integer;
  configured_max integer;
  recovery_action text;
begin
  if stale_after_seconds < 60 then
    raise exception 'stale_after_seconds must be at least 60';
  end if;

  if default_max_attempts < 1 or default_max_attempts > 10 then
    raise exception 'default_max_attempts must be between 1 and 10';
  end if;

  for rec in
    select *
    from public.task_queue
    where status = 'in_progress'
      and coalesce(
        lease_expires_at,
        claimed_at + make_interval(secs => stale_after_seconds),
        updated_at + make_interval(secs => stale_after_seconds)
      ) < now()
    order by coalesce(lease_expires_at, updated_at) asc
    for update skip locked
  loop
    configured_max := coalesce(rec.max_attempts, default_max_attempts);
    next_attempt := coalesce(rec.attempt_count, 0) + 1;

    if next_attempt >= configured_max then
      recovery_action := 'failed';

      update public.task_queue
      set
        status = 'failed',
        attempt_count = next_attempt,
        result_summary = coalesce(result_summary, 'Task failed after stale in-progress recovery exceeded max attempts.'),
        lease_expires_at = null,
        claimed_at = null,
        assigned_agent = null,
        updated_at = now()
      where id = rec.id;
    else
      recovery_action := 'requeued';

      update public.task_queue
      set
        status = 'pending',
        attempt_count = next_attempt,
        lease_expires_at = null,
        claimed_at = null,
        assigned_agent = null,
        updated_at = now(),
        metadata = coalesce(metadata, '{}'::jsonb) || jsonb_build_object('last_stale_recovery_at', now())
      where id = rec.id;
    end if;

    insert into public.execution_log(task_id, action_type, details, outcome, error_message)
    values (
      rec.id,
      'stale_task_recovery',
      jsonb_build_object(
        'action', recovery_action,
        'previous_attempt_count', coalesce(rec.attempt_count, 0),
        'attempt_count', next_attempt,
        'max_attempts', configured_max,
        'previous_assigned_agent', rec.assigned_agent,
        'previous_lease_expires_at', rec.lease_expires_at
      ),
      case when recovery_action = 'failed' then 'failure'::execution_outcome else 'success'::execution_outcome end,
      case when recovery_action = 'failed' then 'Stale task exceeded max attempts' else null end
    );

    task_id := rec.id;
    action := recovery_action;
    attempt_count := next_attempt;
    max_attempts := configured_max;
    return next;
  end loop;
end;
$$;

